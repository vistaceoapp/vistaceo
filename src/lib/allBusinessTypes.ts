// Helper function to get business types for a specific area
export function getBusinessTypesForArea(areaId: string): BusinessTypeData[] {
  return ALL_BUSINESS_TYPES[areaId] || [];
}

// Helper function to get a specific business type by ID across all areas
export function getBusinessTypeById(typeId: string): BusinessTypeData | undefined {
  for (const types of Object.values(ALL_BUSINESS_TYPES)) {
    const found = types.find(t => t.id === typeId);
    if (found) return found;
  }
  return undefined;
}
