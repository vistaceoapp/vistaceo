// Helper para obtener preguntas por tipo de negocio y modo
export const getGastroQuestionsForBusiness = (
  businessTypeId: string,
  mode: 'quick' | 'complete'
): GastroCompleteQuestion[] => {
  return GASTRO_COMPLETE_QUESTIONS.filter(q => {
    // Filtrar por modo
    if (q.mode !== 'both' && q.mode !== mode) return false;
    
    // Si no tiene businessTypes, aplica a todos
    if (!q.businessTypes) return true;
    
    // Verificar si el tipo est√° incluido
    return q.businessTypes.includes(businessTypeId);
  });
};

// Contar preguntas por modo y tipo
export const countGastroQuestions = (businessTypeId: string) => {
  const quick = getGastroQuestionsForBusiness(businessTypeId, 'quick').length;
  const complete = getGastroQuestionsForBusiness(businessTypeId, 'complete').length;
  return { quick, complete };
};
